export function detectCategory(text: string): FaqCategory {
  return detectCategoryMatch(text).category;
}

export function detectCategoryMatch(text: string): { category: FaqCategory; score: number } {
  const normalized = text.toLowerCase();
  const ranked = faqCategories
    .map((category) => ({
      category,
      score: category.keywords.reduce((total, keyword) => {
        return normalized.includes(keyword.toLowerCase()) ? total + 1 : total;
      }, 0),
    }))
    .sort((a, b) => b.score - a.score);

  return ranked[0]?.score > 0 ? ranked[0] : { category: fallbackCategory, score: 0 };
}
