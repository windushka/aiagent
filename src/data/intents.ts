function includesAny(text: string, keywords: string[]) {
  return keywords.some((keyword) => text.includes(keyword));
}

export function analyzeIntent(text: string): IntentResult {
  const normalized = text.trim().toLowerCase();

  const generalAnswer = generalAnswers.find((item) => includesAny(normalized, item.keywords));
  const categoryMatch = detectCategoryMatch(normalized);
  const hasIssueIndicator = includesAny(normalized, issueIndicators);

  if (categoryMatch.score > 0 && hasIssueIndicator) {
    return {
      type: 'ticket',
      category: categoryMatch.category,
      confidence: Math.min(95, 55 + categoryMatch.score * 15),
    };
  }

  if (generalAnswer && !hasIssueIndicator) {
    return {
      type: 'general',
      answer: generalAnswer.answer,
    };
  }

  if (categoryMatch.score > 1) {
    return {
      type: 'ticket',
      category: categoryMatch.category,
      confidence: Math.min(90, 50 + categoryMatch.score * 12),
    };
  }

  return {
    type: 'unknown',
    answer:
      'Я не уверен, это общий вопрос или инцидент. Если нужно оформить обращение, опишите проблему словами вроде "не работает", "нет доступа", "ошибка" и укажите кабинет или систему.',
  };
}
